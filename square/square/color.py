class Colors:
    """
    Class to colorize output

    Attributes:
        blue -- set terminal color output to blue
        green -- set terminal color output to green
        cyan -- set terminal color output to cyan
        reset -- reset color
    """

    BLUE = "\033[94m"
    CYAN = "\033[96m"
    GREEN = "\033[92m"
    RESET = "\033[0m"
